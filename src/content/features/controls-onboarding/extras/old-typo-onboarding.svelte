<h3>Introducing the new typo</h3>

<p>
  Starting with April 2025, typo is receiving the biggest update so far.<br>
  Typo still has the same features, but makes them much more user-friendly, accessible and customizable.<br>
  So far, typo provided the tools, but now it's the whole workshop!
</p>

<h4>A quick-start guide</h4>

<p>
  All typo features can now be customized, so you don't have anything that messes with your optimal skribbl experience.<br>
  To customize typo, click the wrench icon at the top left (unless customized to somewhere else ;).<br>
  There, you can:
</p>

<ul>
  <li>Enable or disable every feature</li>
  <li>Adjust feature settings to your likes</li>
  <li>Customize hotkeys and chat commands of features</li>
  <li>View some extra info & hints about features</li>
</ul>

<p>
  To get started, you can browse through the feature list and view their details.<br>
  Features are also categorized by tags so you can easily find what you are looking for.<br>
  Most of the features did not change much, but you might discover some new ones or notice features that you didn't know of!<br>
  To change general typo settings or reset typo, choose the "Typo Settings" feature in the settings.<br>
  If you have any questions or feedback, join the community on <a href="https://discord.com/invite/pAapmUmWAM">Discord</a>!
</p>

<h4>The most important changes</h4>

<div>
  <p>
    <b>➜ Typo Settings</b><br>
    Typo settings are now located directly on skribbl (read the quick-start-guide ;) instead of the extension popup menu.<br>
    You can customize everything that you're used to from the popup menu, but now found in the respective feature dialog.<br>
  </p>

  <p>
    <b>➜ Lobby Privacy</b><br>
    Instead of the lock icon at the very left side, a connection icon is displayed at the top-right corner of the chat.<br>
    An icon with a checkmark means that the lobby has privacy settings active (not visible to everyone).<br>
    If you're the lobby owner (first typo player to join), you can change lobby settings there.<br>
  </p>

  <p>
    <b>➜ Filter Search</b><br>
    To start a lobby search with filters, you need to use the "Start Search" button in the "Filters" tab on the home screen.<br>
    Clicking "Play" will always just join the next public/private lobby.<br>
  </p>

  <p>
    <b>➜ Chat Commands</b><br>
    Chat commands received a massive facelift!<br>
    To start using commands, type a "/" in the chat box. <br>
    A popout with available commands will open; you can continue typing the command and arguments and submit the command with "Enter".<br>
    You can also customize the "/" prefix to another character in the settings of the "Chat Commands" feature.<br>
  </p>

  <p>
    <b>➜ QuickReact and Hotkeys</b><br>
    Hotkeys are now individually customizable for every feature.<br>
    As an example, QuickReact uses now exclusively the right Ctrl key.<br>
    To change hotkeys for any feature, open its feature settings.<br>
    Hotkeys can also consist of combinations.<br>
  </p>

  <p>
    <b>➜ Zoom Drawing</b><br>
    Zooming still uses the Ctrl hotkey, but starts now immediately at the last cursor position without clicking on the canvas.<br>
    To exit zoom, click Ctrl again.<br>
  </p>

  <p>
    <b>➜ Custom Colors</b><br>
    When you use custom colors in public lobbies, typo will now show the closest skribbl color for non-typo users.<br>
    It is still recommended that you use custom colors only in lobbies with typo players, because fill behavior might appear differently for non-typo-users.<br>
  </p>

  <p>
    <b>➜ Practice Lobby</b><br>
    Instead of clicking your avatar on the home screen, you can enter the offline free draw mode using the palette icon next to your avatar.<br>
  </p>

</div>
